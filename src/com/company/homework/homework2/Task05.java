package com.company.homework.homework2;

public class Task05 {

    static final int CIRCLE_RADIUS = 10;
    static final float PI = 3.1415926f;

    public static void main(String[] args) {

        float areaCircle = PI * CIRCLE_RADIUS * CIRCLE_RADIUS;
        float lenthCircle = 2 * PI * CIRCLE_RADIUS;

        System.out.println("Radius is " + CIRCLE_RADIUS + ". Circle area is " + areaCircle);
        System.out.println("Circle lenth is " + lenthCircle);

    }
}
